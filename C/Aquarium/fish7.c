void pattern(char *p,int n)
{
	int l=strlen(p),i,x=0;//,n1=(n+l>100)?100-l:n;
	n=n%100;
	if(n+l>100)
	{
		x=(100-n)+1;
		for(i=1;i<=l-(100-n);i++)
			printf("%c",p[x++]);
		x=0;
		for(i=1;x<=100-n;i++)
			printf("%c",(i>=100-l)?p[x++]:' ');
	}
	else
		for(i=1;i<=100;i++)
			printf("%c",(i>=n && i<=n+l)?p[x++]:' ');
	printf("\n");
}

