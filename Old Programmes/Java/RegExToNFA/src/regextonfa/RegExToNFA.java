	private String addDot(String s) {
		for(int i=1; i<s.length(); i++)
			if((Character.isAlphabetic(s.charAt(i)) || "({[".contains(""+s.charAt(i)))
				&& ((Character.isAlphabetic(s.charAt(i-1)) || ")}]*+".contains(""+s.charAt(i-1))))) {
				s=s.substring(0, i)+"."+s.substring(i);
				i++;
			}
		return s;
	}
	
	int precedence(char op) {
		switch(op) {
			case '|': return 0;
			case '.': return 1;
			case '*': return 2;
			case '+': return 2;
			default:  return 0;
		}
	}
	private String in2post(String s) {
		char temp;
		String postfix="";
		Stack<Character> stk=new Stack<>();
		for(int i=0; i<s.length(); i++) {
			if("({[".contains(s.substring(i, i+1))){
				stk.push(s.charAt(i));
			} else if(")}]".contains(s.substring(i, i+1))) {
				temp=stk.peek();
				char begin=(s.charAt(i)==')')?'(':(s.charAt(i)=='}')?'{':'[';
				while(temp!=begin) {
					postfix+=stk.pop();
					temp=stk.peek();
				}
				stk.pop();
			} else if(Character.isDigit(s.charAt(i))) {
				while(i<s.length() && Character.isDigit(s.charAt(i))) {
					postfix+=s.charAt(i);
					i++;
				}
				i--;
			} else if(Character.isAlphabetic(s.charAt(i))) {
				while(i<s.length() && Character.isAlphabetic(s.charAt(i))) {
					postfix+=s.charAt(i);
					i++;
				}
				i--;
			} else if("|.*+".contains(s.substring(i, i+1))) {
				if(stk.empty())
					stk.push(s.charAt(i));
				else {
					temp=stk.pop().charValue();
					stk.push(temp);
					while(temp!='(' && temp!='{' && temp!='[' && precedence(s.charAt(i))<=precedence(temp)) {
						postfix+=stk.pop();
						if(!stk.empty())
							temp=stk.peek();
						else {
							stk.push(s.charAt(i));
							break;
						}
					}
					if(temp=='(' || temp=='{' || temp=='[' || precedence(s.charAt(i))>precedence(temp))
						stk.push(s.charAt(i));
				}
			}
		}
		while(!stk.empty())
			postfix+=stk.pop();
		return postfix;
	}
	
