;MERGE 2 ASCENDING SORTED LIST TO 1 ASCENDING SORTED LIST
;INPUT	: 00A0H, 00B0H
;OUTPUT	: 00C0H
	LXI SP, 0FFFFH
	LXI H, 00C0H
	XTHL
	LXI H, 00B0H
	MOV C, M		;C=LENGTH OF LIST 00B0H IN DE
	INX H
	XCHG
	LXI H, 00A0H
	MOV B, M		;B=LENGTH OF LIST 00A0H IN HL
	INX H
L1:	MOV A, M
	XCHG
	CMP M
	XCHG
	JNC ELSE
	XTHL
	MOV M, A
	INX H
	XTHL
	INX H
	DCR B
	JNZ L1
	JMP L3
ELSE:	XCHG
	MOV A, M
	XCHG
	XTHL
	MOV M, A
	INX H
	XTHL
	INX D
	DCR C
	JNZ L1
L2:	MOV A, M
	XTHL
	MOV M, A
	INX H
	XTHL
	INX H
	DCR B
	JNZ L2
	JMP END
L3:	XCHG
	MOV A, M
	XCHG
	XTHL
	MOV M, A
	INX H
	XTHL
	INX D
	DCR C
	JNZ L3
END:	HLT